#include "push_swap.h"

void	free_args(char **args)
{
	int	i;

	i = 0;
	while (args[i])
	{
		free(args[i]);
		i++;
	}
	free(args);
}

t_list	*find_first(t_list *a)
{
	while (a->i != 0)
		a = a->next;
	return (a);
}
